/** word → Set of alternative spellings (union of all sets containing the word). */
const MAP = new Map();
for (const set of HOMOPHONE_SETS) {
  for (const word of set) {
    let bucket = MAP.get(word);
    if (!bucket) {
      bucket = new Set();
      MAP.set(word, bucket);
    }
    for (const other of set) if (other !== word) bucket.add(other);
  }
}

/** Alternative spellings for a normalized (uppercase) token. Never includes the token itself. */
export function homophonesOf(word) {
  return [...(MAP.get(word) ?? [])];
}

